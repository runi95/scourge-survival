const COMMAND_PREFIX = "-";

export class Commands {
  private readonly gameMap: GameMap;
  private readonly gameOptions: GameOptions;
  private readonly player: MapPlayer;

  constructor(gameOptions: GameOptions, gameMap: GameMap, player: MapPlayer) {
    this.gameOptions = gameOptions;
    this.gameMap = gameMap;
    this.player = player;

    const trig = Trigger.create();
    trig.addAction(() => this.handleCommand());
    trig.registerPlayerChatEvent(player, "", false);
  }

  private handleCommand(): void {
    const input = GetEventPlayerChatString();
    if (!input.startsWith(COMMAND_PREFIX)) {
      return;
    }
    const parts = input.substring(COMMAND_PREFIX.length).split(" ");
    const command = parts[0].toLowerCase();
    const args = parts.length > 1 ? parts.slice(1) : [];

    switch (command) {
      case "zoom":
      case "cam":
        if (GetLocalPlayer() === this.player.handle) {
          const amount: number = parseInt(args[0]);
          if (!amount) {
            // player.sendMessage(Util.ColourString(COLOUR_CODES[COLOUR.RED], 'Invalid Amount'));
            return;
          }
          SetCameraField(CAMERA_FIELD_TARGET_DISTANCE, amount, 1);
        }
        break;
      case "tilt":
        if (GetLocalPlayer() === this.player.handle) {
          const amount: number = parseInt(args[0]);

          SetCameraField(CAMERA_FIELD_ANGLE_OF_ATTACK, amount, 1);
        }
        break;
    }

    if (!this.gameOptions.isDebugModeEnabled) return;

    switch (command) {
      case "f":
        if (args.length === 1) {
          const vehicle = this.gameMap.playerVehicles[0];
          print(vehicle);
          print(
            BlzGetAbilityRealField(
              vehicle.unit.getAbility(FourCC("A000")),
              ConvertAbilityRealField(parseInt(args[0]))
            )
          );
        }
        break;
      case "list":
        (() => {
          const selectedUnits = GetUnitsSelectedAll(Player(0));
          const selectedUnit = BlzGroupUnitAt(selectedUnits, 0);
          for (let i = 0; i < 10; i++) {
            const ability = BlzGetUnitAbilityByIndex(selectedUnit, i);
            const code = BlzGetAbilityId(ability);
            print(`${GetAbilityName(code)}: ${I2S(code)}`);
          }
        })();
        break;
      case "child":
        (() => {
          // None of these:
          // const topMsg = Frame.fromOrigin(ORIGIN_FRAME_TOP_MSG, 0);
          // topMsg.setVisible(false);
          // const chatMsg = Frame.fromOrigin(ORIGIN_FRAME_CHAT_MSG, 0);
          // chatMsg.setVisible(false);
          // const uberTooltip = Frame.fromOrigin(ORIGIN_FRAME_UBERTOOLTIP, 0);
          // uberTooltip.setVisible(false);
          // const world = Frame.fromOrigin(ORIGIN_FRAME_WORLD_FRAME, 0);
          // world.setVisible(false);

          // Somewhere here!
          const index = args.length === 1 ? Number(args[0]) : 0;
          const origin = Frame.fromOrigin(ORIGIN_FRAME_GAME_UI, 0);
          // const child = origin.getChild(5).getChild(index);
          const child = origin.getChild(5);
          const childCount = child.childrenCount;
          print(childCount);

          // child.setVisible(false);
          // child.setVisible(false);

          // if (args.length === 1) {
          //   DisplayTextToPlayer(
          //     GetLocalPlayer(),
          //     0.5,
          //     0,
          //     "|cffffcc00Unable to drop this item.|r"
          //   );
          // }
          SetIntroShotText("Hello???");
        })();
        break;
      case "add":
        (() => {
          const vehicle = this.gameMap.playerVehicles[0];
          // vehicle.unit.addAbility(FourCC("A000"));
          // SetPlayerAbilityAvailable(Player(0), FourCC("A000"), false);
          // BlzUnitDisableAbility(
          //   vehicle.unit.handle,
          //   FourCC("A000"),
          //   true,
          //   true
          // );
          // const didAdd = UnitAddAbility(vehicle.unit.handle, FourCC("A000"));
          // print(`didAdd? ${didAdd}`);
          // SetPlayerAbilityAvailable(Player(0), FourCC("A000"), true);
        })();
        break;
      case "list":
        (() => {
          const vehicle = this.gameMap.playerVehicles[0];
          for (let i = 0; i < 10; i++) {
            const ability = vehicle.unit.getAbilityByIndex(i);
            const abilityId = BlzGetAbilityId(ability);
            print(`${i}: ${GetAbilityName(abilityId)}`);
          }
        })();
        break;
      case "hide":
        this.gameMap.playerVehicles[0].unit.disableAbility(
          FourCC("AHbu"),
          false,
          true
        );
        break;
      case "debug":
        Log.Init([new StringSink(LogLevel.Debug, print)]);
    }
  }
}

